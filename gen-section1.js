const MM = (mm) => convertMillimetersToTwip(mm);

function TR(text, opts = {}) {
  return new TextRun({ text, font: 'Times New Roman', size: opts.size || 28, bold: opts.bold || false, italics: opts.italics || false });
}

function Para(text, opts = {}) {
  return new Paragraph({
    children: [TR(text, opts)],
    alignment: opts.align || AlignmentType.JUSTIFIED,
    spacing: { before: opts.before || 0, after: opts.after || 0, line: opts.line || 360 },
    indent: { firstLine: opts.fl !== undefined ? opts.fl : MM(12.5), left: opts.l !== undefined ? opts.l : MM(5), right: opts.r !== undefined ? opts.r : MM(5) },
  });
}

function Head(text, lvl = 1) {
  return new Paragraph({
    children: [TR(text, { size: 28, bold: true })],
    alignment: AlignmentType.JUSTIFIED,
    spacing: { before: 0, after: lvl === 1 ? 240 : 120, line: 360 },
    indent: { firstLine: MM(12.5), left: MM(5), right: MM(5) },
    heading: lvl === 1 ? HeadingLevel.HEADING_1 : HeadingLevel.HEADING_2,
  });
}

function Bullet(text) { return Para('\u2013 ' + text, { before: 0, after: 0 }); }

function Empty() { return Para('', { fl: 0 }); }

function Center(text, opts = {}) {
  return new Paragraph({
    children: [TR(text, opts)],
    alignment: AlignmentType.CENTER,
    spacing: { before: opts.before || 0, after: opts.after || 0, line: 360 },
    indent: { left: opts.l || 0, right: opts.r || 0 },
  });
}

function Tbl(headers, rows) {
  const tableRows = [
    new TableRow({
      children: headers.map(h => new TableCell({
        children: [new Paragraph({ children: [TR(h, { size: 24, bold: true })], alignment: AlignmentType.CENTER, spacing: { before: 0, after: 0, line: 240 } })],
        borders: { top: { style: BorderStyle.SINGLE, size: 1, color: '000000' }, bottom: { style: BorderStyle.SINGLE, size: 1, color: '000000' }, left: { style: BorderStyle.SINGLE, size: 1, color: '000000' }, right: { style: BorderStyle.SINGLE, size: 1, color: '000000' } },
      })),
    }),
    ...rows.map(row => new TableRow({
      children: row.map(cell => new TableCell({
        children: [new Paragraph({ children: [TR(cell, { size: 24 })], alignment: AlignmentType.CENTER, spacing: { before: 0, after: 0, line: 240 } })],
        borders: { top: { style: BorderStyle.SINGLE, size: 1, color: '000000' }, bottom: { style: BorderStyle.SINGLE, size: 1, color: '000000' }, left: { style: BorderStyle.SINGLE, size: 1, color: '000000' }, right: { style: BorderStyle.SINGLE, size: 1, color: '000000' } },
      })),
    })),
  ];
  return new Table({ rows: tableRows, width: { size: 100, type: WidthType.PERCENTAGE } });
}

